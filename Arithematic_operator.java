public class Arithematic_operator {
    public static void main(String[] args) {
        int a = 30;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        
        //Increment
        int c = 12;
        ++c;
        System.out.println(c);

        //Decrement
        int d = 23;
        --d;
        System.out.println(d);
        
        

        
    }
}
